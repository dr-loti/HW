
mood = input("Hi, how do you feel or in what mood are you today?: ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up! Everything's gonna be alright.")
elif mood == "excited":
    print("Great, that you are so excited you just cant hide it")
elif mood == "relaxed":
    print("Stay relaxed and keep enjoying your day.")
elif mood == "bored":
    print("Start to learn how to program with python and hours will just fly by. Trust me.")
else:
    print("I don't recognize this mood")